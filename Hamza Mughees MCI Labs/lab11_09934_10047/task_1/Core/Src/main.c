// Function to write data 
void I3G_WriteReg(uint8_t reg_addr, uint8_t data) {
    uint8_t tx_buffer[2];
    tx_buffer[0] = reg_addr & 0x7F; // Bit 7 = 0 for WRITE
    tx_buffer[1] = data;
    
    HAL_GPIO_WritePin(GPIOE, GPIO_PIN_3, GPIO_PIN_RESET);
    HAL_SPI_Transmit(&hspi1, tx_buffer, 2, HAL_MAX_DELAY);
    HAL_GPIO_WritePin(GPIOE, GPIO_PIN_3, GPIO_PIN_SET);
}

// Function to read data 
uint8_t I3G_ReadReg(uint8_t reg_addr) {
    uint8_t tx_data = reg_addr | 0x80; // Bit 7 = 1 for READ
    uint8_t rx_data;
    
    HAL_GPIO_WritePin(GPIOE, GPIO_PIN_3, GPIO_PIN_RESET);
    HAL_SPI_Transmit(&hspi1, &tx_data, 1, HAL_MAX_DELAY);
    HAL_SPI_Receive(&hspi1, &rx_data, 1, HAL_MAX_DELAY);
    HAL_GPIO_WritePin(GPIOE, GPIO_PIN_3, GPIO_PIN_SET);
    
    return rx_data;
}

// --- LSM303DLHC Accelerometer I2C Helper Functions ---
#define ACC_I2C_ADDR 0x32 // 8-bit I2C address for the accelerometer

// Function to write data to the Accelerometer
void LSM303_WriteReg(uint8_t reg_addr, uint8_t data) {
    HAL_I2C_Mem_Write(&hi2c1, ACC_I2C_ADDR, reg_addr, I2C_MEMADD_SIZE_8BIT, &data, 1, HAL_MAX_DELAY);
}

// Function to read data from the Accelerometer
uint8_t LSM303_ReadReg(uint8_t reg_addr) {
    uint8_t rx_data;
    HAL_I2C_Mem_Read(&hi2c1, ACC_I2C_ADDR, reg_addr, I2C_MEMADD_SIZE_8BIT, &rx_data, 1, HAL_MAX_DELAY);
    return rx_data;
}
