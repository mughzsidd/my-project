/* USER CODE BEGIN 4 */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
    if (htim->Instance == TIM3) {
        static float internal_tilt = 0.0f;
        static int uart_div = 0;

        // 1. READ GYROSCOPE (SPI) 
        uint8_t y_low_g = I3G_ReadReg(0x2A);
        uint8_t y_high_g = I3G_ReadReg(0x2B);
        int16_t raw_gyro_y = (int16_t)((y_high_g << 8) | y_low_g);
        float gyro_y_dps = (float)raw_gyro_y * 0.00875f;

        // 2. READ ACCELEROMETER (I2C)
        // Read Y-axis
        uint8_t y_low_a = LSM303_ReadReg(0x2A);
        uint8_t y_high_a = LSM303_ReadReg(0x2B);
        int16_t acc_y = (int16_t)((y_high_a << 8) | y_low_a);

        // Read Z-axis
        uint8_t z_low_a = LSM303_ReadReg(0x2C);
        uint8_t z_high_a = LSM303_ReadReg(0x2D);
        int16_t acc_z = (int16_t)((z_high_a << 8) | z_low_a);

        // Multiply Angle by 57.2958 to convert Radians to Degrees
        float acc_angle_deg = atan2f((float)acc_y, (float)acc_z) * 57.2958f;

        // Update global variable for debugging/plotting 
        acc_angle = acc_angle_deg; 
        gyro_rate = gyro_y_dps;

        // 3. COMPLEMENTARY FILTER 
        internal_tilt = 0.98f * (internal_tilt + gyro_y_dps * 0.005f) + 0.02f * acc_angle_deg;
        shared_angle = internal_tilt;

        // 4. UART THROTTLING 
        if (++uart_div >= 20) {
            display_flag = 1;
            uart_div = 0;
        }
    }
}
